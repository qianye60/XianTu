export const ACTION_OPTIONS_RULES = `
# 行动选项规范

## ⚠️ 重要：自定义要求（必须严格遵守）
{{CUSTOM_ACTION_PROMPT}}

## 输出格式

在 JSON 响应中**必须**新增 \`action_options\` 字段：

\`\`\`json
{
  "text": "叙事文本...",
  "mid_term_memory": "记忆摘要...",
  "tavern_commands": [...],
  "action_options": [
    "选项1的描述",
    "选项2的描述",
    "选项3的描述"
  ]
}
\`\`\`

## 生成规则

1. **数量**：每次生成 3-5 个行动选项
2. **内容**：
   - 基于当前场景和角色状态
   - 提供不同方向的行动可能性
   - 简洁明了，每个选项 10-20 字
3. **类型**：
   - 对话类：与NPC交谈、询问、说服
   - 行动类：移动、探索、使用物品
   - 修炼类：打坐、修炼功法、突破境界
   - 战斗类：攻击、防御、使用技能
   - 社交类：结交、拜访、参加活动
4. **禁止**：
   - 不要生成过于具体的选项（如"向左走3步"）
   - 不要生成玩家无法执行的选项
   - 不要生成重复或相似的选项

## 示例

**场景：在宗门外门广场**
\`\`\`json
{
  "action_options": [
    "前往藏经阁查阅功法",
    "与附近的外门弟子交谈",
    "前往修炼室打坐修炼",
    "查看宗门任务榜",
    "返回自己的住处休息"
  ]
}
\`\`\`

**场景：遇到敌对修士**
\`\`\`json
{
  "action_options": [
    "立即出手攻击",
    "尝试沟通化解矛盾",
    "转身逃离此地",
    "观察对方实力再做决定"
  ]
}
\`\`\`

**场景：修炼遇到瓶颈**
\`\`\`json
{
  "action_options": [
    "继续苦修尝试突破",
    "寻找师长请教",
    "前往藏经阁寻找突破之法",
    "暂时放下修炼外出历练",
    "服用丹药辅助突破"
  ]
}
\`\`\`
`.trim()
